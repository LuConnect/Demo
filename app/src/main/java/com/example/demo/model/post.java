package com.example.demo.model;


import java.util.Date;

public class post {

    private String image, user, caption;
    private Date time;

    public String getImage() {
        return image;
    }

    public String getUser() {
        return user;
    }

    public String getCaption() {
        return caption;
    }

    public Date getTime() {
        return time;
    }
}
